#include "main.h"

/**
 * mul - function declaration
 * Description: find product of 2 numbers
 * @a: first number
 * @b: second number
 * Return: product
 */

int mul(int a, int b);

/**
 * mul - function definition
 * Description: multiply 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: the product
 */

int mul(int a, int b)
{
	int product = a * b;

	return (product);
}
